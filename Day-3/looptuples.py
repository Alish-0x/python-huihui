# looping through a tuple
fruits = ("apple", "banana", "cherry")
for fruit in fruits:
    print(fruit)


# looping through the index of a tuple
fruits = ("apple", "banana", "cherry")
for i in range(len(fruits)):
    print(fruits[i])

# using while loop to iterate through a tuple
fruits = ("apple", "banana", "cherry")
i = 0
while i < len(fruits):
    print(fruits[i])
    i += 1

