# Tuples are immutable sequences in Python
my_tuple = (1, 2, 3, 'a', 'b', 'c')
print(my_tuple)

#its ordered,unchangeable, and allows duplicate values


#length of a tuple - `len()`
print(len(my_tuple))

#creating tuple with one item
single_item_tuple = (5,)
print(single_item_tuple)

#not a tuple
not_a_tuple = (5)
print(type(not_a_tuple))


#tuple constructor `tuple()`
my_tuple = tuple((1, 2, 3, 'a', 'b', 'c'))
print(my_tuple)


