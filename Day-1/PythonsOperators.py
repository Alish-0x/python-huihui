#Operators
#we use + operator to add two values 
print(3 + 5)

#we use - operator to subtract two values
print(10 - 2)

#we use * operator to multiply two values
print(4 * 7)
#we use / operator to divide two values
print(20 / 4)

#we use % operator to get the remainder of a division
print(10 % 3)   

#we use // operator to get the quotient of a division
print(10 // 3)
#we use ** operator to get the power of a number
print(2 ** 3)

#we use == operator to check if two values are equal
print(5 == 5)

#we use != operator to check if two values are not equal
print(5 != 3)   
#we use > operator to check if a value is greater than another value
print(7 > 4)
#we use < operator to check if a value is less than another value
print(3 < 6)

#we use >= operator to check if a value is greater than or equal to another value
print(5 >= 5)

#we use <= operator to check if a value is less than or equal to another value
print(4 <= 6)

#we use and operator to check if two conditions are true
print(5 > 3 and 4 < 6)

#we use or operator to check if at least one condition is true
print(5 > 3 or 4 > 6)

#we use not operator to reverse the result of a condition
print(not(5 > 3))

#we use in operator to check if a value is present in a sequence
print('a' in 'apple')   




