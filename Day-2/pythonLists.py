# Lists - used to store multiple items in a single variable 
#its created using square brackets
thislist = ["apple", "banana", "cherry"]
print(thislist)

#just like arrays - it can hold different data types and are indexed from 0 and so on

thislist = ["apple", 1, 2.5, True]
print(thislist)

#list items are ordered, changeable, and allow duplicate values.

thisxlist = ["apple", "banana", "cherry", "apple", "cherry"]
print(thisxlist)

#we can use the len() and type() function to determine how many items a list has and data type of list just like we used in strings
print(len(thisxlist))
print(type(thisxlist))




